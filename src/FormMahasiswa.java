    private void tblMahasiswaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMahasiswaMouseClicked
        int baris = tblMahasiswa.rowAtPoint(evt.getPoint());
        String nim = tblMahasiswa.getValueAt(baris, 0).toString();
        txtNim.setText(nim);
        String nama = tblMahasiswa.getValueAt(baris, 1).toString();
        txtNama.setText(nama);
        String alamat = tblMahasiswa.getValueAt(baris, 2).toString();
        txtAlamat.setText(alamat);
    }//GEN-LAST:event_tblMahasiswaMouseClicked

    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        try {
            if (txtNim.getText().trim().isEmpty() || txtNama.getText().trim().isEmpty() || txtAlamat.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Pilih dulu data yang mau diubah dari tabel!");
                return;
            }

            String sql = "UPDATE mahasiswa SET nama='" + txtNama.getText() 
                    + "', alamat='" + txtAlamat.getText() 
                    + "' WHERE nim='" + txtNim.getText() + "'";
            
            Connection conn = Koneksi.configDB();
            java.sql.PreparedStatement pstm = conn.prepareStatement(sql);
            pstm.execute();
            
            JOptionPane.showMessageDialog(null, "Data Berhasil Diubah!");
            tampil_data();

            txtNim.setText("");
            txtNama.setText("");
            txtAlamat.setText("");
            txtNim.requestFocus();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Ubah Data Gagal: " + e.getMessage());
        }
    }//GEN-LAST:event_btnUbahActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        try {
            if (txtNim.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Pilih dulu data yang mau dihapus dari tabel!");
                return;
            }

            int konfirmasi = JOptionPane.showConfirmDialog(null, "Yakin ingin menghapus data dengan NIM " + txtNim.getText() + "?", "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);
            
            if (konfirmasi == JOptionPane.YES_OPTION) {
                String sql = "DELETE FROM mahasiswa WHERE nim='" + txtNim.getText() + "'";
                Connection conn = Koneksi.configDB();
                java.sql.PreparedStatement pstm = conn.prepareStatement(sql);
                pstm.execute();
                
                JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus!");
                tampil_data();
                
                txtNim.setText("");
                txtNama.setText("");
                txtAlamat.setText("");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Hapus Data Gagal: " + e.getMessage());
        }
    }//GEN-LAST:event_btnHapusActionPerformed

    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("NIM");
        model.addColumn("Nama");
        model.addColumn("Alamat");
        tblMahasiswa.setModel(model);
        
        try {
            String cari = txtCari.getText(); 
            Connection conn = Koneksi.configDB();
            Statement stm = conn.createStatement();

            ResultSet res = stm.executeQuery("SELECT * FROM mahasiswa WHERE nim LIKE '%" + cari + "%' OR nama LIKE '%" + cari + "%'");
            
            while (res.next()) {
                model.addRow(new Object[]{
                    res.getString("nim"),
                    res.getString("nama"),
                    res.getString("alamat")
                });
            }

            txtCari.setText("");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Pencarian Gagal: " + e.getMessage());
        }
    }//GEN-LAST:event_btnCariActionPerformed

