    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        try {

            String nim = txtNim.getText();
            String nama = txtNama.getText();
            String alamat = txtAlamat.getText();
            if (nim.trim().isEmpty() || nama.trim().isEmpty() || alamat.trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Semua kolom (NIM, Nama, Alamat) tidak boleh kosong!");
                return; 
            }

            if (!nim.matches("[0-9]+")) {
                JOptionPane.showMessageDialog(this, "NIM harus berupa angka murni!");
                txtNim.selectAll();
                txtNim.requestFocus();
                return; 
            }
            if (!nama.matches("^[a-zA-Z\\s\\.,']+$")) {
                JOptionPane.showMessageDialog(null, "Nama hanya boleh berisi huruf, spasi, titik, koma, atau tanda kutip!");
                txtNama.selectAll();
                txtNama.requestFocus();
                return; 
            }
            String sql = "INSERT INTO mahasiswa (nim, nama, alamat) VALUES ('"
                    + nim + "','"
                    + nama + "','"
                    + alamat + "')";
            
            Connection conn = Koneksi.configDB();
            java.sql.PreparedStatement pstm = conn.prepareStatement(sql);
            pstm.execute();
            
            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan!");

            tampil_data(); 
            txtNim.setText("");
            txtNama.setText("");
            txtAlamat.setText("");
            txtNim.requestFocus();
            
        } catch (Exception e) {

            JOptionPane.showMessageDialog(this, "Simpan Data Gagal (NIM mungkin sudah terdaftar): " + e.getMessage());
        }
    }//GEN-LAST:event_btnTambahActionPerformed

