    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Kode Mata Kuliah");
        model.addColumn("Nama Mata Kuliah");
        model.addColumn("SKS");
        model.addColumn("Semester");
        tblMatakuliah.setModel(model);

        try {
            String cari = txtCari.getText();
            Connection conn = Koneksi.configDB();
            Statement stm = conn.createStatement();
            ResultSet res = stm.executeQuery("SELECT * FROM matakuliah WHERE kode_mk LIKE '%" + cari + "%' OR nama_mk LIKE '%" + cari + "%'");

            while (res.next()) {
                model.addRow(new Object[]{
                    res.getString("kode_mk"), res.getString("nama_mk"),
                    res.getString("sks"), res.getString("semester")
                });
            }
            txtCari.setText("");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Pencarian Gagal: " + e.getMessage());
        }
    }//GEN-LAST:event_btnCariActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        try {
            if (txtKode.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Pilih data yang mau dihapus dari tabel!");
                return;
            }

            int konfirmasi = JOptionPane.showConfirmDialog(null, "Yakin hapus Mata Kuliah " + txtNama.getText() + "?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
            if (konfirmasi == JOptionPane.YES_OPTION) {
                String sql = "DELETE FROM matakuliah WHERE kode_mk='" + txtKode.getText() + "'";
                Connection conn = Koneksi.configDB();
                java.sql.PreparedStatement pstm = conn.prepareStatement(sql);
                pstm.execute();

                JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus!");
                tampil_data();
                kosongkan_form();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Hapus Gagal: " + e.getMessage());
        }
    }//GEN-LAST:event_btnHapusActionPerformed

    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        try {
            String kode = txtKode.getText();
            String nama = txtNama.getText();
            String sks = txtSks.getText();
            String smt = txtSemester.getText();

            if (kode.trim().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Pilih data yang mau diubah dari tabel!");
                return;
            }
            if (!sks.matches("[0-9]+") || !smt.matches("[0-9]+")) {
                JOptionPane.showMessageDialog(null, "SKS dan Semester harus berupa angka!");
                return;
            }

            String sql = "UPDATE matakuliah SET nama_mk='" + nama
            + "', sks='" + sks + "', semester='" + smt
            + "' WHERE kode_mk='" + kode + "'";

            Connection conn = Koneksi.configDB();
            java.sql.PreparedStatement pstm = conn.prepareStatement(sql);
            pstm.execute();

            JOptionPane.showMessageDialog(null, "Data Berhasil Diubah!");
            tampil_data();
            kosongkan_form();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Ubah Data Gagal: " + e.getMessage());
        }
    }//GEN-LAST:event_btnUbahActionPerformed

    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        try {
            String kode = txtKode.getText();
            String nama = txtNama.getText();
            String sks = txtSks.getText();
            String smt = txtSemester.getText();
            if (kode.trim().isEmpty() || nama.trim().isEmpty() || sks.trim().isEmpty() || smt.trim().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Semua kolom tidak boleh kosong!");
                return;
            }
            if (!sks.matches("[0-9]+") || !smt.matches("[0-9]+")) {
                JOptionPane.showMessageDialog(null, "SKS dan Semester harus berupa angka murni!");
                return;
            }

            String sql = "INSERT INTO matakuliah (kode_mk, nama_mk, sks, semester) VALUES ('"
            + kode + "','" + nama + "','" + sks + "','" + smt + "')";

            Connection conn = Koneksi.configDB();
            java.sql.PreparedStatement pstm = conn.prepareStatement(sql);
            pstm.execute();

            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan!");
            tampil_data();
            kosongkan_form();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Simpan Gagal (Kode MK mungkin sudah ada): " + e.getMessage());
        }
    }//GEN-LAST:event_btnTambahActionPerformed

