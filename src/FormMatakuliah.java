private void tampil_data() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Kode Mata Kuliah");
        model.addColumn("Nama Mata Kuliah");
        model.addColumn("SKS");
        model.addColumn("Semester");
        tblMatakuliah.setModel(model);

        try {
            Connection conn = Koneksi.configDB();
            Statement stm = conn.createStatement();
            ResultSet res = stm.executeQuery("SELECT * FROM matakuliah");

            while (res.next()) {
                model.addRow(new Object[]{
                    res.getString("kode_mk"),
                    res.getString("nama_mk"),
                    res.getString("sks"),
                    res.getString("semester")
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal memuat data: " + e.getMessage());
        }
    }
